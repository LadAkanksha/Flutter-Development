//Arithmatic Operator

void main() {
  num N1 = 50;
  num N2 = 5;
  num Res = 0;

  Res = N1 + N2;
  print("Addition :       $N1 + $N2 = $Res");

  Res = N1 - N2;
  print("Substraction :   $N1 - $N2 = $Res");

  Res = N1 * N2;
  print("Multiplication : $N1 * $N2 = $Res");

  Res = N1 / N2;
  print("Division :       $N1 / $N2 = $Res");
}
