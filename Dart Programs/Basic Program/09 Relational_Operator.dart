//  Relational Operator

void main() {
  num a = 10;
  num b = 20;
  var Res;

  Res = a > b;
  print("$a > $b is $Res");

  Res = a < b;
  print("$a < $b is $Res");

  Res = a <= b;
  print("$a <= $b is $Res");

  Res = a >= b;
  print("$a >= $b is $Res");

  Res = a != b;
  print("$a != $b is $Res");

  Res = a == b;
  print("$a == $b is $Res");
}
